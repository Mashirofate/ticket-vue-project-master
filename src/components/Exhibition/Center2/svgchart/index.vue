        const entrance = newVal[i].number

        document.getElementById(id + '-text').textContent = entrance

        const sum = parseInt(maps.get(id))
        var color = ''
        var mun = (entrance / sum) * 10000

        // console.log('id:' + id + ' entrance' + entrance + ' sum' + sum + ' mun' + mun)
        if (mun < 50) {
          color = '#0AE2A4'
          this.colors(id, color)
        } else if (mun < 70) {
          color = '#0DB0FF'
          this.colors(id, color)
        } else if (mun < 90) {
          color = '#1AE9F0'
          this.colors(id, color)
        } else if (mun < 100) {
          color = '#FFA340'
          this.colors(id, color)
        } else if (mun >= 100) {
          color = '#FF1801'
          this.colors(id, color)
        }
      }
    },

